void perimetreRond(double rayon, double perimetre){
    return perimetre = 2 * 3.14 * rayon;
}

void aireRond(double rayon, double aire){
    return aire = 3.14 * rayon * rayon;
}

void perimetreCarre(double cote, double perimetre){
    return perimetre = 4 * cote;
}

void aireCarre(double cote, double aire){
    return aire = cote * cote;
}

void perimetreRectangle(double largeur, double longueur, double perimetre){
    return perimetre = 2 * (largeur + longueur);
}

void aireRectangle(double largeur, double longueur, double aire){
    return aire = largeur * longueur;
}